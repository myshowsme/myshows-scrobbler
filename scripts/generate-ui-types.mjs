import fs from 'node:fs'
import path from 'node:path'
import process from 'node:process'
import * as ts from 'typescript'

const root = process.cwd()
const sourcePath = path.join(root, 'src', 'types.ts')
const targetPath = path.join(root, 'ui', 'src', 'types.ts')

const publicExports = new Set([
  'SourceType',
  'SOURCE_TYPES',
  'LOCAL_SOURCE_TYPES',
  'TOKEN_ONLY_SOURCE_TYPES',
  'LogLevel',
  'SourceConfig',
  'AppConfig',
  'AppConfigSnapshot',
  'SourceErrorCode',
  'PlaybackAction',
  'PlaybackState',
  'MediaInfo',
  'ExternalIds',
  'NormalizedEvent',
  'NowPlayingEntry',
  'ScrobbleEvent',
  'PollingLog',
  'WsMessage',
])

function exportedName(node) {
  if (
    ts.isInterfaceDeclaration(node) ||
    ts.isTypeAliasDeclaration(node) ||
    ts.isEnumDeclaration(node)
  ) {
    return node.name.text
  }

  if (ts.isVariableStatement(node)) {
    const declaration = node.declarationList.declarations[0]
    return ts.isIdentifier(declaration?.name) ? declaration.name.text : null
  }

  return null
}

function hasExportModifier(node) {
  return node.modifiers?.some((modifier) => modifier.kind === ts.SyntaxKind.ExportKeyword) ?? false
}

function generate() {
  const source = fs.readFileSync(sourcePath, 'utf8').replace(/\r\n/g, '\n')
  const sourceFile = ts.createSourceFile(sourcePath, source, ts.ScriptTarget.Latest, true)
  const chunks = []
  const found = new Set()

  for (const node of sourceFile.statements) {
    if (!hasExportModifier(node)) continue

    const name = exportedName(node)
    if (!name || !publicExports.has(name)) continue

    found.add(name)
    chunks.push(source.slice(node.getStart(sourceFile), node.end).trim())
  }

  const missing = [...publicExports].filter((name) => !found.has(name))
  if (missing.length > 0) {
    throw new Error(`Missing public exports in ${sourcePath}: ${missing.join(', ')}`)
  }

  return [
    '// This file is generated by scripts/generate-ui-types.mjs from src/types.ts.',
    '// Do not edit by hand.',
    '',
    chunks.join('\n\n'),
    '',
  ].join('\n')
}

const generated = generate()

if (process.argv.includes('--check')) {
  const current = fs.existsSync(targetPath)
    ? fs.readFileSync(targetPath, 'utf8').replace(/\r\n/g, '\n')
    : ''

  if (current !== generated) {
    console.error(
      `${path.relative(root, targetPath)} is out of date. Run vp run generate:ui-types.`,
    )
    process.exit(1)
  }

  console.log(`${path.relative(root, targetPath)} is up to date.`)
  process.exit(0)
}

fs.writeFileSync(targetPath, generated)
console.log(`Generated ${path.relative(root, targetPath)} from ${path.relative(root, sourcePath)}.`)
